    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
            
try{
            Statement statement=(Statement) Koneksi.GetConnection().createStatement();
            ResultSet res = statement.executeQuery("select * from surat_keluar where "+"no_urut='"+no1.getText()+"'");
            DefaultTableModel tbl = new DefaultTableModel();
        tbl.addColumn("No_Urut");
        tbl.addColumn("Struktur");
        tbl.addColumn("Substantif");
        tbl.addColumn("Fasilitatif");
        tbl.addColumn("Wilayah");
        tbl.addColumn("Bulan");
        tbl.addColumn("Tahun");
        tbl.addColumn("Tanggal");
        tbl.addColumn("Keterangan");
        tbl.addColumn("Penanggung_Jawab");

            jTable1.setModel(tbl);

            while (res.next()){
                tbl.addRow(new Object[] {
            res.getString("no_urut"),
            res.getString("struktur"),
            res.getString("substantif"),
            res.getString("fasilitatif"),
            res.getString("wilayah"),
            res.getString("bulan"),
            res.getString("tahun"),
            res.getString("tanggal"),
            res.getString("keterangan"),
            res.getString("penanggung_jawab"),
                });
                jTable1.setModel(tbl);
                urut.setText(res.getString("no_urut"));
                struk.setSelectedItem(res.getString("struktur"));
                subtan.setSelectedItem(res.getString("substantif"));
                fasil.setSelectedItem(res.getString("fasilitatif"));
                wil.setSelectedItem(res.getString("wilayah"));
                bul.setText(res.getString("bulan"));
                thn.setText(res.getString("tahun"));
                tgl.setText(res.getString("tanggal"));
                ket.setSelectedItem(res.getString("Keterangan"));
                pj.setText(res.getString("penanggung_jawab"));
                
            }       
         }
        catch (Exception e){
            JOptionPane.showMessageDialog(rootPane,"Salah"+e);
        }   
    }//GEN-LAST:event_jButton5ActionPerformed

