    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        String no_urut = urut.getText();
        String struktur = (String) struk.getSelectedItem();
        String substantif = (String) subtan.getSelectedItem();
        String fasilitatif = (String) fasil.getSelectedItem();
        String wilayah = (String) wil.getSelectedItem();
        String bulan = bul.getText();
        String tahun = thn.getText();
        String tanggal = tgl.getText();
        String keterangan = (String) ket.getSelectedItem();
        String penanggung_jawab = pj.getText();
        
        try{
            Statement statement = (Statement) Koneksi.GetConnection().createStatement();
            statement.executeUpdate("insert into surat_keluar VALUES ('"+ no_urut +"','"+ struktur +"','"+ substantif +"','"+ fasilitatif +"','"+ wilayah +"','"+ bulan +"','"+ tahun +"','"+ tanggal+"','"+ keterangan +"','"+ penanggung_jawab+"');");
            statement.close();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            urut.setText("");
            struk.setSelectedItem("Pilih");
            subtan.setSelectedItem("Pilih");
            fasil.setSelectedItem("Pilih");
            wil.setSelectedItem("Pilih");
            bul.setText("");
            thn.setText("");
            ket.setSelectedItem("Pilih");
            pj.setText("");

        }catch (Exception t){
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan");
        }
        datatable();     
    }//GEN-LAST:event_simpanActionPerformed

